
// Operadores de comparacion

// Comparar si 2 es igual a 6 (true o false)
const resultado01 = 2 === 6

// Igualdad no estricta, string pueden ser iguales a numeros
const resultado02 = 5 == '5'  // true

// Igualdad estricta
const resultado03 = 5 === '5' // false

console.log(resultado01)
console.log(resultado02)
console.log(resultado03)

// Operadores menor o mayor que
const mayorQue = 5 > 2
const menorQue = 10 < 20
const menorIgual = 10 <= 20
const mayorIgual = 10 >= 20
console.log(mayorQue)
console.log(menorQue)
console.log(menorIgual)
console.log(mayorIgual)

const iguales01 = 5 != 6
const iguales02 = 5 !== '5'
console.log(iguales01)
console.log(iguales02)